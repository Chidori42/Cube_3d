#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list			*tmp;

	tmp = 0;
	if (lst && new)
	{
		if (*lst)
		{
			tmp = ft_lstlast(*lst);
			tmp->next = new;
		}
		else
			*lst = new;
	}
}
